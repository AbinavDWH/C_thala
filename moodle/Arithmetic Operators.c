#include<stdio.h>
int main()
{
    int num1 = 10,num2 = 3;
    printf("Addition Result = %d\n",(num1 + num2));
    printf("Subtraction Result = %d\n",(num1 - num2));
    printf("Multiplication Result = %d\n",(num1 * num2));
    printf("Division Result = %d\n",(num1 / num2));
    printf("Remainder = %d",(num1 % num2));
    return 0;
}